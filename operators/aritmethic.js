let x = 10;

// ++
console.log(x++); // x = x + 1 
console.log(x);

// -- 
console.log(x--);
console.log(x);

// += like x = x + 5
console.log(x += 5); // x = x + 5  === x + 5  // x += 10 === x = x + 10

// -= like x = x - 5
console.log(x -= 5);


// *=  like x = x * 5
console.log(x *= 5);

// /=  like x = x / 0
console.log(x /= 0);

